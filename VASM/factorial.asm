; Вычисление факториала числа 5 (5! = 5 * 4 * 3 * 2 * 1 = 120)
SET R1 5       ; Число для вычисления факториала
SET R2 1       ; Аккумулятор результата
SET R3 1       ; Счетчик для декремента
SET R4 1       ; Константа 1 для сравнения

:loop
MOV R3 R1      ; Копируем текущее число в R3
MUL R2 R3      ; R2 = R2 * R3 (результат в R2)
SUB R1 R4      ; Уменьшаем число на 1
CMP R1 R4      ; Сравниваем с 1
JNE loop       ; Если не 1, продолжаем цикл
HLT            ; Конец программы - результат в R2 