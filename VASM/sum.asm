; Сложение чисел от 1 до 10 (сумма = 55)
SET R1 0       ; Сумма
SET R2 1       ; Текущее число
SET R3 10      ; Максимальное число
SET R4 1       ; Константа 1 для инкремента

:loop
ADD R1 R2      ; Добавляем текущее число к сумме
ADD R2 R4      ; Увеличиваем текущее число на 1
CMP R2 R3      ; Сравниваем с максимальным числом
JNE loop       ; Если не равно, продолжаем цикл
HLT            ; Конец программы - результат в R1 